using System;
using StrengthTracker2.Core.DataTypes;

namespace StrengthTracker2.Core.Functional.DBEntities.Actors
{
    public class User
    {
        /// <summary>
        /// Holds the unique identifier information of the logged in user
        /// </summary>
        public int UserId { get; set; }

        /// <summary>
        /// Holds the Username information of the logged in user
        /// </summary>
        public string UserName { get; set; }

        /// <summary>
        /// Holds the Password information of the logged in user
        /// </summary>
        public string Password { get; set; }

        /// <summary>
        /// Holds the First Name information of the Contact
        /// </summary>
        public string FirstName { get; set; }

        /// <summary>
        /// Holds the Last Name information of the Contact
        /// </summary>
        public string LastName { get; set; }

        /// <summary>
        /// Holds the Date of Birth information of the logged in user
        /// </summary>
        public DateTime DOB { get; set; }

        /// <summary>
        /// Holds the Gender information of the logged in user
        /// </summary>
        public Gender Gender { get; set; }

        /// <summary>
        /// Holds the boolean value for account enabled information of the logged in user
        /// </summary>
        public bool IsAccountEnabled { get; set; }

        /// <summary>
        /// Holds UserType information (Admin / Franchisee / Client ...)
        /// </summary>
        public UserType UserType { get; set; }

        /// <summary>
        /// Holds Grade info for User
        /// </summary>
        public int Grade { get; set; }

        /// <summary>
        /// Holds selected sports for User
        /// </summary>
        public int SportID { get; set; }

        /// <summary>
        /// Position the User would be Training for
        /// </summary>
        public int PositionID { get; set; }

        /// <summary>
        /// Program chosen after Free Evaluation
        /// </summary>
        public int FinalizedProgram { get; set; }

        /// <summary>
        /// Program Price
        /// </summary>
        public decimal FinalizedPrice { get; set; }

        /// <summary>
        /// Payment mode
        /// </summary>
        public int PaymentMode { get; set; }

        /// <summary>
        /// TrackID selected while enabling the user
        /// </summary>
        public int TrackID { get; set; }

        /// <summary>
        /// User needs to reset password
        /// </summary>
        public bool ResetPassword { get; set; }

        /// <summary>
        /// ProgramTimeID
        /// </summary>
        public System.Nullable<int> ProgramTimeID { get; set; }

        /// <summary>
        /// YearsOfPlayOrgSport
        /// </summary>
        public System.Nullable<int> YearsOfPlayOrgSport { get; set; }

        /// <summary>
        /// InjuryConcern
        /// </summary>
        public System.Nullable<int> InjuryConcern { get; set; }

        /// <summary>
        /// PlayingOnSchool
        /// </summary>
        public System.Nullable<int> PlayingInSchoolTeam { get; set; }

        /// <summary>
        /// SchoolName
        /// </summary>
        public string SchoolName { get; set; }

        /// <summary>
        /// ParticipateOnTravelTeam
        /// </summary>
        public System.Nullable<int> ParticipateInTravelTeam { get; set; }

        /// <summary>
        /// TravelTeamName
        /// </summary>
        public string TravelTeamName { get; set; }

        /// <summary>
        /// PlayingLevel
        /// </summary>
        public System.Nullable<int> PlayingLevel { get; set; }

        /// <summary>
        /// Enablement Date for the user
        /// </summary>
        public DateTime EnablementDate { get; set; }

        /// <summary>
        /// Where the did the user heard from? Default is 4 = Promotional Event
        /// </summary>
        public int HeardFrom { get; set; }

        /// <summary>
        /// if the payment method is chq, for storing chq num issued by the client
        /// </summary>
        public string ChqNumber { get; set; }

        /// <summary>
        /// Has client agreed to Photo usage policy
        /// </summary>
        public bool AgreedToPhotoPolicy { get; set; }

        /// <summary>
        /// Would the user pay later. 0 = Not required, 1=PayLater enabled, 2=Paid
        /// </summary>
        public PayLater PayLater { get; set; }

        /// <summary>
        /// Record is deleted or not
        /// </summary>
        public bool IsDeleted { get; set; }

        public bool IsPending { get; set; }

        /// <summary>
        /// Associated Coach ID
        /// </summary>
        public int CoachID { get; set; }

        /// <summary>
        /// Associated TeamID
        /// </summary>
        public int TeamID { get; set; }

        /// <summary>
        /// Is user Athletic Director
        /// </summary>
        public bool IsAthleticDirector { get; set; }

        /// <summary>
        /// Is User Strength Coach
        /// </summary>
        public bool IsStrengthCoach { get; set; }

        /// <summary>
        /// Type of coach - other, coach, sport or team
        /// </summary>
        public int CoachType { get; set; }

        /// <summary>
        /// Show welcome page for user
        /// </summary>
        public bool ShowWelcome { get; set; }

        public string RegisteredSport { get; set; }

        /// <summary>
        /// For specifying if the user is an individual athlete - For TKG only
        /// </summary>
        public bool IsIndividualAthlete { get; set; }

        public string OtherGrade { get; set; }
    }
}
