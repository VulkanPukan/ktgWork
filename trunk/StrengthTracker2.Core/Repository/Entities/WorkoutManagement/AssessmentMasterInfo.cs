using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace StrengthTracker2.Core.Repository.Entities.WorkoutManagement
{
    public class AssessmentMasterInfo
    {
        /// <summary>
        /// primary key
        /// </summary>
        public int ID { get; set; }

        /// <summary>
        /// Associated Athlete
        /// </summary>
        public int UserID { get; set; }

        /// <summary>
        /// Associated Team
        /// </summary>
        public int TeamID { get; set; }

        /// <summary>
        /// Associated Coach
        /// </summary>
        public int CoachID { get; set; }

        /// <summary>
        /// Associated Sport
        /// </summary>
        public int SportID { get; set; }

        /// <summary>
        /// Associated Position
        /// </summary>
        public int PositionID { get; set; }

        /// <summary>
        /// Associated Program
        /// </summary>
        public int ProgramID { get; set; }

        /// <summary>
        /// Assessment Number
        /// </summary>
        public int AssessmentNumber { get; set; }

        /// <summary>
        /// Record create date time
        /// </summary>
        public DateTime CreateDate { get; set; }

        /// <summary>
        /// Record update date time
        /// </summary>
        public DateTime UpdateDate { get; set; }
    }
}
