using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace StrengthTracker2.Core.Repository.Entities.WorkoutManagement
{
    public class UserWorkoutSessionInfo
    {
        /// <summary>
        /// Primary Key
        /// </summary>
        public int ID { get; set; }

        /// <summary>
        /// Associated UserID
        /// </summary>
        public int UserID { get; set; }

        /// <summary>
        /// Associated ProgramID
        /// </summary>
        public int ProgramID { get; set; }

        /// <summary>
        /// Associated TrackID
        /// </summary>
        public int TrackID { get; set; }

        /// <summary>
        /// Associated SessionID
        /// </summary>
        public int SessionID { get; set; }

        /// <summary>
        /// Associated ExerciseID
        /// </summary>
        public int ExerciseID { get; set; }

        /// <summary>
        /// Associated Step
        /// </summary>
        public int Step { get; set; }

        /// <summary>
        /// Associcated Group
        /// </summary>
        public int Group { get; set; }

        /// <summary>
        /// Weight used by the Athelete while exercising
        /// </summary>
        public int Load { get; set; }

        /// <summary>
        /// Reps performed by the Athelete
        /// </summary>
        public int Reps { get; set; }

        /// <summary>
        /// Associated Desired Reps
        /// </summary>
        public int DesiredReps { get; set; }

        /// <summary>
        /// Associated Session Num
        /// </summary>
        public int SessionNumber { get; set; }

        /// <summary>
        /// Calculated load
        /// </summary>
        public decimal CalculatedLoad { get; set; }

        /// <summary>
        /// Set/Rep completed by the User
        /// </summary>
        public string ResponseVals { get; set; }

        /// <summary>
        /// Date and time when the data was added/modified
        /// </summary>
        public DateTime ModifiedDate { get; set; }

        /// <summary>
        /// Target Reps for the exercise
        /// </summary>
        public int TargetReps { get; set; }

        /// <summary>
        /// Status 
        /// </summary>
        public int Status { get; set; }
    }
}
