using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace StrengthTracker2.Core.Repository.Entities.ProgramManagement
{
    public class ProgramSpecialSchedule
    {
        /// <summary>
        /// primary key
        /// </summary>
        public int ID { get; set; }

        /// <summary>
        /// Special schedule for days of week
        /// </summary>
        public string Days { get; set; }

        /// <summary>
        /// Group associated with the special schedule
        /// </summary>
        public int ExerciseForGroup { get; set; }

        /// <summary>
        /// Associated Program
        /// </summary>
        public int ProgramID { get; set; }
    }
}
